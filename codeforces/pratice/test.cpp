#include <bits/stdc++.h>
using namespace std;

template <typename T> 
inline T wow(T n) {
	return n * n;
}

int main(int argc, char const *argv[])
{
	ios::sync_with_stdio(false);

	double a = 2.005450033;
	int b = 4;
	long long int c = 23;


	cout << wow(a) << " " << wow(b) << " " << wow(c) ;
	return 0;
}
